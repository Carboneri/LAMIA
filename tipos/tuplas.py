nomes = ("João", "Maria", "José", "Ana", "Carlos")  
# tupla -> parecida com lista, mas imutavel

print('Bia' in nomes)  
# verifica se existe

print(nomes[0])  
# acesso por indice

print(nomes[1:3])  
# fatia (slice)

print(nomes[1:-1])  
# do segundo ate o penultimo

print(nomes[2:])  
# do indice 2 ate o final

print(nomes[:-2])  
# do inicio ate antes dos 2 ultimos

print(len(nomes))  
# tamanho

print(type(nomes))  # tuple
print(nomes)